@router.post("/generate", status_code=202)
async def generate_report(
    request: ReportRequest,
    background_tasks: BackgroundTasks,
) -> dict:
    """
    Queue report generation as a background task.

    Returns report_id immediately. Poll GET /reports/{id} to check status.
    Download via GET /reports/{id}/download when status = 'ready'.
    """
    report_id = str(uuid.uuid4())[:8]
    _REPORTS[report_id] = {
        "id": report_id,
        "title": request.title,
        "format": request.format,
        "created_at": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "status": "queued",
        "file_path": None,
        "file_size_bytes": None,
    }

    background_tasks.add_task(_run_report_generation, report_id, request)

    return {
        "report_id": report_id,
        "status": "queued",
        "message": "Report generation started",
        "poll_url": f"/api/v1/reports/{report_id}",
    }


@router.get("", response_model=list[ReportMeta])
def list_reports() -> list[ReportMeta]:
    """List all generated reports."""
    return [ReportMeta(**r) for r in _REPORTS.values()]


@router.get("/{report_id}", response_model=ReportMeta)
def get_report(report_id: Annotated[str, FPath()]) -> ReportMeta:
    """Get report metadata and generation status."""
    if report_id not in _REPORTS:
        raise HTTPException(status_code=404, detail=f"Report {report_id} not found")
    return ReportMeta(**_REPORTS[report_id])


@router.get("/{report_id}/download")
def download_report(report_id: Annotated[str, FPath()]) -> FileResponse:
    """Download generated report file (JSON or PDF)."""
    if report_id not in _REPORTS:
        raise HTTPException(status_code=404, detail=f"Report {report_id} not found")

    meta = _REPORTS[report_id]
    if meta["status"] != "ready":
        raise HTTPException(
            status_code=409,
            detail=f"Report not ready yet (status: {meta['status']})",
        )

    path = meta.get("file_path")
    if not path or not Path(path).exists():
        raise HTTPException(status_code=404, detail="Report file not found on disk")

    fmt = meta.get("format", "json")
    media_types = {
        "json": "application/json",
        "pdf": "application/pdf",
        "csv": "text/csv",
    }

    return FileResponse(
        path=path,
        media_type=media_types.get(fmt, "application/octet-stream"),
        filename=f"trichome_report_{report_id}.{fmt}",
    )


@router.delete("/{report_id}")
def delete_report(report_id: Annotated[str, FPath()]) -> dict:
    """Delete report metadata and optionally the file."""
    if report_id not in _REPORTS:
        raise HTTPException(status_code=404, detail=f"Report {report_id} not found")

    meta = _REPORTS.pop(report_id)
    path = meta.get("file_path")
    if path:
        try:
            Path(path).unlink(missing_ok=True)
        except Exception:
            pass

    return {"message": f"Report {report_id} deleted"}
